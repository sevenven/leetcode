// https://leetcode-cn.com/problems/valid-sudoku/
// 请你判断一个 9 x 9 的数独是否有效。只需要 根据以下规则 ，验证已经填入的数字是否有效即可。
// 数字 1-9 在每一行只能出现一次。
// 数字 1-9 在每一列只能出现一次。
// 数字 1-9 在每一个以粗实线分隔的 3x3 宫内只能出现一次。（请参考示例图）

// 注意：
// 一个有效的数独（部分已被填充）不一定是可解的。
// 只需要根据以上规则，验证已经填入的数字是否有效即可。
// 空白格用 '.' 表示。


/**
 * @param {character[][]} board
 * @return {boolean}
 */
var isValidSudoku = function(board) {
	var rows = [],
		cols = [],
		boxs = [];
	for (var i = 0; i < 9; i++) {
		rows[i] = {};
		cols[i] = {};
		boxs[i] = {};
	}
	for (var i = 0; i < 9; i++) { // 列
		for (var j = 0; j < 9; j++) { // 行
			if (board[i][j] === '.') continue;
			var boxIdx = Math.floor(j / 3) + Math.floor(i / 3) * 3,
				num = board[i][j];
			cols[i][num] = cols[i][num] === undefined ? 1 : ++cols[i][num];
			rows[j][num] = rows[j][num] === undefined ? 1 : ++rows[j][num];
			boxs[boxIdx][num] = boxs[boxIdx][num] === undefined ? 1 : ++boxs[boxIdx][num];
			if (cols[i][num] > 1 || rows[j][num] > 1 || boxs[boxIdx][num] > 1) return false;
 		}
	}
	return true;
};

